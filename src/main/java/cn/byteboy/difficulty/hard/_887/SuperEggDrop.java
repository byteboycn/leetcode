public class SuperEggDrop {

    @Solution
    public int superEggDrop(int k, int n) {
        return dp(k, n);
    }

    // dp(k, n)è¡¨ç¤ºkä¸ªé¸¡è›‹ï¼Œnå±‚æ¥¼æ—¶æœ€å°æ“ä½œæ¬¡æ•°æ—¶dp(k, n)
    private int dp(int k, int n) {
        // å¦‚æœåªæœ‰ä¸€ä¸ªé¸¡è›‹ï¼Œé‚£ä¹ˆå¿…é¡»è¦ä»1æ¥¼å°è¯•è‡³næ¥¼ï¼Œæœ€åæƒ…å†µå°±æ—¶næ¬¡
        if (k == 1) return n;
        if (n == 0) return 0;
        int res = Integer.MAX_VALUE;
        for (int i = 1; i < n + 1; i++) {
            res = Math.min(res, Math.max(dp(k, n - i), dp(k - 1, i - 1)) + 1);
        }

        return res;
    }
}
